#include <stdio.h>
#include <stdlib.h>
/**
 * main - entry point of the program,
 * description: prints numbers 0-10, followed by a new line
 * Return:Always 0 (Success)
 */
int main(void)
{
	int m;
	/*loop for to print numbers 0-10 */
	for (m = 0; m <= 10; m++)
	{	printf("%d", m);
	}
	printf("\n");
	return (0);
}
